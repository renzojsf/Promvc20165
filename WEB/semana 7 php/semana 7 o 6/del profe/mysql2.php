<p><strong>mysql_affected_rows</strong></p>
<p>int mysql_affected_rows(int [link_identifier] );</p>
<p>mysql_affected_rows devuelve el n&uacute;mero de filas afectado en el &uacute;ltimo        SELECT, UPDATE o DELETE pregunta en el servidor asociado con el        identificador de conexi&oacute;n especificado. Si no se e especifica un        identificador de conexi&oacute;n, se asume le de la &uacute;ltima conexi&oacute;n abierta.</p>
<p>Este orden no es eficaz para las instrucciones SELECT, s&oacute;lo en        instrucciones que modifican archivos. Para recuperar el n&uacute;mero de filas        vuelto de un SELECT, usa mysql_num_rows.</p>
